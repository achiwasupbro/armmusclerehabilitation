// Show help modal
function showHelp(helpType) {
    console.log('showHelp called with:', helpType);
    
    if (!helpModal) {
        console.error('Help modal not found');
        return;
    }
    
    const helpInfo = helpData[helpType];
    if (!helpInfo) {
        console.error('No help info found for:', helpType);
        return;
    }
    
    if (helpModalTitle) {
        helpModalTitle.textContent = helpInfo.title;
    }
    
    if (helpModalContent) {
        helpModalContent.innerHTML = helpInfo.content;
    }
    
    // Show modal with animation
    helpModal.style.display = 'block';
    helpModal.style.opacity = '0';
    
    setTimeout(() => {
        helpModal.style.opacity = '1';
    }, 10);
    
    console.log('Modal displayed');
}

// Close help modal
function closeHelp() {
    if (!helpModal) return;
    
    helpModal.style.opacity = '0';
    setTimeout(() => {
        helpModal.style.display = 'none';
    }, 300);
}

// Handle arm selection
function selectArm(armType, buttonElement) {
    // Remove active class from all arm buttons
    document.querySelectorAll('.btn-arm').forEach(btn => {
        btn.classList.remove('active');
    });

    // Add active class to selected button
    buttonElement.classList.add('active');
    
    console.log('Selected arm:', armType);
}

